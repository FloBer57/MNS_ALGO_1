print("Ceci est un petit script python pour calculer le volume d'un parallélépipède")

a = float(input("Veuillez rentrer la largeur (en cm) : (Appuyez sur 'entrée' pour continuer) "))
b = float(input("Veuillez rentrer la longueur (en cm) : (Appuyez sur 'entrée' pour continuer) "))
c = float(input("Veuillez rentrer la hauteur (en cm) : (Appuyez sur 'entrée' pour continuer) "))

vol = a*b*c

print(f"le résultat pour une largeur {a} ,une longueur {b} et une hauteur {c} est : {vol}")